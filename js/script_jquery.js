jQuery(document).ready(function() {

        jQuery("#subirTopo").hide();

        jQuery('a#subirTopo').click(function() {
            jQuery('html').animate({
                scrollTop: 0
            }, 800);
            return false;
        });

        jQuery(window).scroll(function() {
            if (jQuery(this).scrollTop() > 1000) {
                jQuery('#subirTopo').fadeIn();
            } else {
                jQuery('#subirTopo').fadeOut();
            }
        });
    });